public class Method
{
    /// <summary>
    /// Tính tổng của 2 int a, b và trả về tổng int tong 
    /// </summary>
    /// <param name="a"></param>
    /// a: int
    /// <param name="b"></param>
    /// int b
    /// <returns>
    /// kết quả output trả về là tổng kiểu int
    /// 
    /// </returns>
    public static int TinhTong(int a = 0, int b = 0)
    {

        int tong = 0;
        tong = a + b;

        return tong;
    }
}